--CRIANDO UMA TABELA
CREATE TABLE ALUNO(
	ID SERIAL,
	NOME VARCHAR(255),
	CPF CHAR(11),
	OBSERVACAO TEXT,
	IDADE INTEGER,
	DINHEIRO NUMERIC(10,2),
	ALTURA REAL,
	ATIVO BOOLEAN,
	DATA_NASCIMENTO DATE,
	HORA_AULA TIME,
	MATRICULADO TIMESTAMP
);


